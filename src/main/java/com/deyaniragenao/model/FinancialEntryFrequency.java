package com.deyaniragenao.model;

/**
 * Enum class that describes how frequently a financial entry may occur. For example, users may have
 * to pay a certain expense on a weekly basis. 
 * @author deyaniragenao
 *
 */
public enum FinancialEntryFrequency {
	WEEKLY,
	BIWEEKLY,
	MONTHLY,
	ANNUALLY
	
}
